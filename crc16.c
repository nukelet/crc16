	// printf("reading\n");
	uint16_t n;
	int ret;
	ret = read(fd, &n, 2);
	if (ret != 2) {
		fprintf(stderr, "couldn't read random uint16_t from /dev/urandom\n");
		exit(1);
	}

	// printf("read: %02x\n", n);

